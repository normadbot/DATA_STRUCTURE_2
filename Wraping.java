import java.lang.*;


public class Wraping{
 
 public static void main (String [] args) {
   
     Integer i2 = new Integer(42);
     
	 byte b = i2.byteValue();
     System.out.println(b);
     
	 short s = i2.shortValue();
     System.out.println(s);
     
	 double d= i2.doubleValue();
     System.out.println(d);
   
   
  
  
  }
 



}